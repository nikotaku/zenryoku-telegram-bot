async def generate_seo_article(template_type: str, custom_keyword: str = "") -> str:
    """
    Gemini APIを使ってSEO記事ドラフトを生成する。

    Args:
        template_type: "ranking" or "howto"
        custom_keyword: ユーザーが指定した追加キーワード（オプション）

    Returns:
        生成された記事テキスト
    """
    try:
        import google.generativeai as genai
        genai.configure(api_key=os.environ.get("GEMINI_API_KEY"))

        model = genai.GenerativeModel("gemini-2.0-flash")

        if template_type == "ranking":
            system_prompt = _get_system_prompt_ranking()
            user_prompt = "仙台のメンズエステ人気ランキングTOP5の記事を作成してください。全力エステを第1位として紹介してください。"
        else:
            system_prompt = _get_system_prompt_howto()
            user_prompt = "メンズエステの選び方で失敗しない7つのコツの記事を作成してください。全力エステを初心者におすすめの店舗として紹介してください。"

        if custom_keyword:
            user_prompt += f"\n\n追加で意識するキーワード: {custom_keyword}"

        response = model.generate_content(
            contents=[
                {"role": "user", "parts": [{"text": f"{system_prompt}\n\n{user_prompt}"}]},
            ],
            generation_config=genai.types.GenerationConfig(
                max_output_tokens=4000,
                temperature=0.7,
            ),
        )

        article = response.text
        return article

    except Exception as e:
        logger.error(f"SEO記事生成エラー: {e}")
        raise


